}

cleanup_on_error() {
    log_error "Setup failed. Cleaning up..."
    docker-compose down 2>/dev/null || true
}

main() {
    trap cleanup_on_error ERR
    
    print_banner
    check_dependencies
    setup_git_config
    create_env_file
    build_docker_image
    show_quick_start
}

# Check if script is being sourced or executed
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi